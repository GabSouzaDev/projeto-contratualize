    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String clientName = jTextField1.getText().toUpperCase();
        String contractNumber = jTextField2.getText();
        String bankCode = jTextField5.getText();
        int numberOfInstallments = (Integer) jSpinner1.getValue();
        String contractLink = jTextField4.getText().toLowerCase();
        
        
        //Captura e conversão da data
        LocalDate startDate;
        try {
            String dateString = jFormattedTextField1.getText().trim();
            startDate = ConfigureDateField.parseDate(dateString);
            
            
        } catch(DateTimeParseException e) {
            JOptionPane.showMessageDialog(this, "Data inválida. Use o formato DD/MM/AAAA.");
            return;
        }
        
        //Inicia validação
        if(clientName.isEmpty() || contractNumber.isEmpty() || bankCode.isEmpty() || numberOfInstallments == 0){
            JOptionPane.showMessageDialog(this, "Por favor, preencha os campos obrigatórios");
            return;
            
        }
        if(!contractLink.isEmpty() && !LinkValidator.isValid(contractLink)){
            JOptionPane.showMessageDialog(this, "Formato de link inválido!" +
                    "certifique que você esteja inserindo um documento PDF ou imagem referente" +
                    "ao contrato a ser cadastrado");

        }
        
        
        if(numberOfInstallments <= 0) {
            JOptionPane.showMessageDialog(this, "A quantidade de parcelas não pode ser negativa.");
        }
        
        ContractDAO contractDAO = new ContractDAO();
        
        Contract contract = new Contract();
        contract.setClientName(clientName);
        contract.setContractNumber(contractNumber);
        contract.setBankCode(bankCode);
        contract.setNumberOfInstallments(numberOfInstallments);
        contract.setStartDate(startDate);
        contract.setContractLink(contractLink);
        
        try {
            contractDAO.insert(contract);
            JOptionPane.showMessageDialog(this, "Contrado registrado com sucesso!");
            clearFields();
        } catch(IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
            
        } catch(Exception e){
            JOptionPane.showMessageDialog(this, "Erro ao registrar o contrato:" + e.getMessage());

        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

    
