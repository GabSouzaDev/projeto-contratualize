   private void updateBankName() {
        String bankCodeStr = jTextField5.getText();
        if (!bankCodeStr.isEmpty()) {
            try {
                int bankCode = Integer.parseInt(bankCodeStr); // Converte para int
                String bankName = fetcher.getBankNameByCode(bankCode);
                if (bankName != null) {
                    jTextField3.setText(bankName); // Preencher o campo do nome do banco
                } else {
                    jTextField3.setText(""); // Limpar se não encontrado
                }
            } catch (NumberFormatException e) {
                jTextField3.setText(""); // Limpar se houver erro na conversão
            }
        } else {
            jTextField3.setText(""); // Limpar se o campo estiver vazio
        }
    }

